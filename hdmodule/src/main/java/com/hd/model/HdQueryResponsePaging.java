public class HdQueryResponsePaging implements Serializable {
    private int pageSize; //每页最大显示记录数，0表示包含全部结果集
    private int page;       //当前页号，从0计数
    private int pageCount; //查询结果集总页数，当属性#isMore()==true时，意味着有可能实际总页数会大于取值。
    private long recordCount; //查询结果集总记录数，当属性#isMore()==true时，意味着有可能实际总记录数会大于取值。。,
    private boolean more; //是否还有更多数据未被发现


    public int getPageSize() {
        return pageSize;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getPageCount() {
        return pageCount;
    }

    public void setPageCount(int pageCount) {
        this.pageCount = pageCount;
    }

    public long getRecordCount() {
        return recordCount;
    }

    public void setRecordCount(long recordCount) {
        this.recordCount = recordCount;
    }

    public boolean isMore() {
        return more;
    }

    public void setMore(boolean more) {
        this.more = more;
    }


    @Override
    public String toString() {
        return "HdQueryResponsePaging{" +
                "pageSize=" + pageSize +
                ", page=" + page +
                ", pageCount=" + pageCount +
                ", recordCount=" + recordCount +
                ", more=" + more +
                '}';
    }
}
