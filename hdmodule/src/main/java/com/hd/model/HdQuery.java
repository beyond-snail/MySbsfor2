import java.io.Serializable;
import java.util.List;

/**********************************************************
 * *
 * Created by wucongpeng on 2017/2/20.        *
 **********************************************************/


public class HdQuery implements Serializable {


    private List<Conditions> conditions;
    private List<Orders> orders;
    private int pageSize;
    private int page;
    private int probePages;


    public List<Conditions> getConditions() {
        return conditions;
    }

    public void setConditions(List<Conditions> conditions) {
        this.conditions = conditions;
    }

    public List<Orders> getOrders() {
        return orders;
    }

    public void setOrders(List<Orders> orders) {
        this.orders = orders;
    }

    public int getPageSize() {
        return pageSize;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getProbePages() {
        return probePages;
    }

    public void setProbePages(int probePages) {
        this.probePages = probePages;
    }

    @Override
    public String toString() {
        return "HdQuery{" +
                "conditions=" + conditions +
                ", orders=" + orders +
                ", pageSize=" + pageSize +
                ", page=" + page +
                ", probePages=" + probePages +
                '}';
    }
}
